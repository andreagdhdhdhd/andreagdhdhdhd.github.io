		<h2>Juegos</h2>

		<div class="juegos">
			<p>Todos estos juegos son para dispositivo móvil y completamente gratuitos, para seguir el link a Play Store solo debes clicar en el nombre del juego.</p>

			<a target="_blank" href="https://play.google.com/store/apps/details?id=com.hidea.cat">Cats & Soup</a>
			<p> Es un juego sencillo en el que podrás ir teniendo un negocio con gatitos haciendo sopas, muy relajante e incluso puedes ponerle ropita a los gatos.
			</p>

			<a target="_blank" href="https://play.google.com/store/apps/details?id=com.curiousmedia.smoosh">Smoosh</a>
			<p> Un juego de rompecabezas en el que tendrás que llevar a cada bolita con su color correspondiente.
			</p>

			<a target="_blank" href="https://play.google.com/store/apps/details?id=com.joxdev.orbia">Orbia</a>
			<p> Orbia es un juego relajante de niveles infinitos. Cuenta con gráficos minimalistas y coloridos que te brindarán una experiencia visual inigualable. También incluye sonidos calmados y mundos diversos que puedes explorar a medida que subes de nivel.
			</p>

			<a target="_blank" href="https://play.google.com/store/apps/details?id=com.buffstudio.myoasis">My Oasis</a>
			<p> Ofrece una interfaz amigable con todo usuario que quiera controlar la ansiedad y desestresarse. Incluye distintos ejercicios de relajación con los que despejar la mente y tomar un merecido descanso.
			</p>

			<a target="_blank" href="https://play.google.com/store/apps/details?id=com.noodlecake.altosadventure">Alto’s Adventure</a>
			<p> Incluye sonidos envolventes que te trasladarán a otro mundo. Dentro del juego, podrás explorar distintos paisajes montañosos, ruinas antiguas y saltar sobre el abismo.
			</p>

			<a target="_blank" href="https://play.google.com/store/apps/details?id=com.zutgames.ilovehue">I love Hue</a>
			<p> Es un juego simple de colores. Sobre todo para amantes de la simetría y armonía de colores. Dentro del juego, podrás encontrar varios rompecabezas o bien seguir tu propio ritmo.
			</p>

